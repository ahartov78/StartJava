public class Variable {
    public static void main(String[] args) {
        byte a = 8;
        System.out.println("Core = " + a);
        short b = 16;
        System.out.println("RAM = " + b);
        int c = 64;
        System.out.println("Bit = " + c);
        long d = 1080;
        System.out.println("Resolution = " + d);
        float e = 1.800f;
        System.out.println("CPU = " + e);
        double f = 2.300;
        System.out.println("CPU1 = " + f);
        char g = 'A';
        System.out.println("Name = " + g);
        boolean h = false;
        System.out.println("DVD = " + h);
    }
}